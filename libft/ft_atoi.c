int		ft_atoi(const char *str)
{
	int i;
	int nb;
	int flag;

	i = 0;
	nb = 0;
	flag = 1;
	while ((str[i] == '\n') || (str[i] == '\t') || (str[i] == '\v')
			|| (str[i] == '\r') || (str[i] == '\f') || (str[i] == ' '))
		i++;
	if ((str[i] == '-') || (str[i] == '+'))
	{
		if (str[i] == '-')
			flag = -1;
		i++;
	}
	while ((str[i] <= '9') && (str[i] >= '0'))
	{
		nb = 10 * nb + (str[i] - '0');
		i++;
	}
	return (nb * flag);
}
