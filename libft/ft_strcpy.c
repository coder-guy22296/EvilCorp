#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int		i;

	i = 0;
	while (1)
	{
		dst[i] = src[i];
		if (dst[i] == '\0')
			break ;
		i++;
	}
	return (dst);
}
