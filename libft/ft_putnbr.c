#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int nb;

	if (n < 0)
	{
		nb = -n;
		ft_putchar('-');
	}
	else
		nb = n;
	if (nb < 10)
		ft_putchar('0' + nb);
	else
	{
		ft_putnbr((int)(nb / 10));
		ft_putchar('0' + (nb - nb / 10 * 10));
	}
}
